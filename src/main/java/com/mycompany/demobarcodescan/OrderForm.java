    private void txt_BarcodeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_BarcodeKeyTyped
        // TODO add your handling code here:
        // Khi người dùng nhấn Enter
    if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
        String barcode = txt_Barcode.getText().trim();

        if (barcode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập hoặc quét barcode!");
            return;
        }

//        String sql = "select p.p_id, p.productName, p.priceOut, s.Qty from tbl_product" + " where p.p_id = s.p_id and p.barcode = '"+txt_Barcode.getText()+"'";
            String sql = "SELECT p.p_id, p.productName, p.priceOut, ISNULL(s.Qty, 0) as Qty " +
             "FROM tbl_product p " +
             "LEFT JOIN tbl_stocks s ON p.p_id = s.p_id " +
             "WHERE p.barcode = ?";



        try (Connection conn = ConnectDatabase.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {
            
            ps.setString(1, barcode);
            
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    product_id = ""+rs.getInt(1);
                    productName = rs.getString(2);
                    price = rs.getDouble(3);
                    qty = rs.getInt(4);

                    if (qty > 0) {
                        txt_ProductName.setText(productName);
                        txt_Price.setText(""+price);
                        txt_Qty.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(this, "Sản phẩm đã hết hàng!");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm với barcode: " + barcode);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn DB: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_txt_BarcodeKeyTyped

    
    
