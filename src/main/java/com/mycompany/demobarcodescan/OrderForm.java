    private int autoOrderId() {
    int id = 0;
    try (Connection con = ConnectDatabase.getConnection();
         PreparedStatement ps = con.prepareStatement("SELECT MAX(orderId) FROM [tbl_order]");
         ResultSet rs = ps.executeQuery()) {

        if (rs.next()) {
            id = rs.getInt(1);
        }
        id++;
    } catch (SQLException e) {
        e.printStackTrace(); // để bạn thấy lỗi gì xảy ra
    }
    return id;
}

    

